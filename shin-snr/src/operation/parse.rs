use shin_versions::{LengthKind, NumberStyle};

use crate::{
    operation::{arena::OperationArena, schema::OperationElement},
    reader::Reader,
};

#[derive(Default)]
pub enum OptionalNumberBit {
    #[default]
    Unknown,
    Present,
    Absent,
}

impl OptionalNumberBit {
    pub fn from_operation_byte(byte: u8) -> Self {
        if byte & 0x80 != 0 {
            Self::Present
        } else {
            Self::Absent
        }
    }
}

#[derive(Default)]
pub struct InstructionParseState {
    pub optional_number_bit: OptionalNumberBit,
}

impl InstructionParseState {
    pub const fn new() -> Self {
        Self {
            optional_number_bit: OptionalNumberBit::Unknown,
        }
    }
}

pub struct InstructionParseContext<'a, 'snr> {
    number_style: NumberStyle,
    reader: &'a mut Reader<'snr>,
    arena: &'a mut OperationArena<'snr>,
    state: InstructionParseState,
}

impl<'a, 'snr> InstructionParseContext<'a, 'snr> {
    pub fn new(
        number_style: NumberStyle,
        reader: &'a mut Reader<'snr>,
        arena: &'a mut OperationArena<'snr>,
    ) -> Self {
        arena.clear();
        Self {
            number_style,
            reader,
            arena,
            state: InstructionParseState::new(),
        }
    }
    pub fn reader_ref(&self) -> &Reader<'_> {
        self.reader
    }

    fn take_u8(&mut self) {
        let value = self.reader.take_u8();
        self.arena.u8s.push(value);
    }
    fn take_u16(&mut self) {
        let value = self.reader.take_u16();
        self.arena.u16s.push(value);
    }
    fn take_u32(&mut self) {
        let value = self.reader.take_u32();
        self.arena.u32s.push(value);
    }

    fn take_operation(&mut self) {
        let op = self.reader.take_u8();
        self.arena.u8s.push(op);
        self.state.optional_number_bit = OptionalNumberBit::from_operation_byte(op);
    }
    fn take_condition(&mut self) {
        let op = self.reader.take_u8();
        self.arena.u8s.push(op);
    }
    fn take_register(&mut self) {
        self.arena.registers.push(self.reader.take_reg());
    }
    fn take_offset(&mut self) {
        self.arena.offsets.push(self.reader.take_offset());
    }
    fn take_number(&mut self) {
        let number = self.reader.take_number(self.number_style);
        self.arena.numbers.push(number);
    }
    fn take_pad_number(&mut self) {
        let number = match self.number_style {
            NumberStyle::U16 => self.reader.take_u16() as u32,
            NumberStyle::VarInt => {
                // NOTE: this will read the padding bits into our fixified varint repr
                self.reader.take_u32()
            }
        };
        self.arena.numbers.push(number);
    }
    fn take_optional_number(&mut self) {
        match self.state.optional_number_bit {
            OptionalNumberBit::Unknown => {
                // this can only happen due to malformed schema
                panic!(
                    "BUG: attempt to read an optional number without setting an optional number bit"
                )
            }
            OptionalNumberBit::Present => self.take_number(),
            OptionalNumberBit::Absent => {
                self.arena.numbers.push(u32::MAX);
            }
        }
    }
    fn take_string(&mut self, length_kind: LengthKind) {
        self.arena
            .strings
            .push(self.reader.take_string(length_kind));
    }
    fn take_string_array(&mut self, length_kind: LengthKind) {
        self.arena
            .string_arrays
            .push(self.reader.take_string_array(length_kind));
    }

    fn take_length(&mut self, kind: LengthKind) -> u16 {
        let length = self.reader.take_length(kind);
        self.arena.lengths.push(length);
        length
    }

    // compounds
    fn take_expression(&mut self) {
        let mut length = 0u16;
        let mut number_count = 0u16;

        loop {
            let code = self.reader.take_u8();
            // TODO: I am a bit unsure whether it's a good idea to push the 0xff terminator
            self.arena.u8s.push(code);
            length = length.checked_add(1).expect("expression too long"); // TODO: fallible parsing?
            match code {
                0x00 => {
                    self.take_number();
                    number_count += 1;
                }
                0xff => break,
                _ => {}
            }
        }

        self.arena.lengths.push(length);
        self.arena.lengths.push(number_count);
    }

    fn take_bitmask_number_array(&mut self) {
        let mask = self.reader.take_u8();
        self.arena.u8s.push(mask);
        let len = mask.count_ones() as u16;
        self.arena.lengths.push(len);
        for _ in 0..len {
            self.take_number();
        }
    }

    fn take_higu_sui_wipe_arg(&mut self) {
        let b1 = self.reader.take_u8();
        let b2 = self.reader.take_u8();

        self.arena.u8s.push(b1);
        self.arena.u8s.push(b2);

        let mask = if b1 == 0 { 0xf } else { 0x1 };
        let len = (b2 & mask).count_ones() as u16;

        self.arena.lengths.push(len);
        for _ in 0..len {
            self.take_number();
        }
    }

    pub fn take_element(&mut self, element: OperationElement) {
        match element {
            OperationElement::U8 => self.take_u8(),
            OperationElement::U16 => self.take_u16(),
            OperationElement::U32 => self.take_u32(),
            OperationElement::Operation => self.take_operation(),
            OperationElement::Condition => self.take_condition(),
            OperationElement::Expression => self.take_expression(),
            OperationElement::Register => self.take_register(),
            OperationElement::RegisterArray(length_kind) => {
                for _ in 0..self.take_length(length_kind) {
                    self.take_register();
                }
            }
            OperationElement::Offset => self.take_offset(),
            OperationElement::OffsetArray(length_kind) => {
                for _ in 0..self.take_length(length_kind) {
                    self.take_offset();
                }
            }
            OperationElement::Number => self.take_number(),
            OperationElement::OptionalNumber => self.take_optional_number(),
            OperationElement::NumberArray(length_kind) => {
                for _ in 0..self.take_length(length_kind) {
                    self.take_number();
                }
            }
            OperationElement::PadNumberArray(length_kind) => {
                for _ in 0..self.take_length(length_kind) {
                    self.take_pad_number();
                }
            }
            OperationElement::BitmaskNumberArray => self.take_bitmask_number_array(),
            OperationElement::String(length_kind) => self.take_string(length_kind),
            OperationElement::StringArray(length_kind) => self.take_string_array(length_kind),
            OperationElement::HiguSuiWipeArg => self.take_higu_sui_wipe_arg(),
        }
    }
}

